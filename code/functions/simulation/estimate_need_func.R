#--------------------------------------------------------------------------------
#--------------------------------------------------------------------------------
# Estimate need for a water supply release
#--------------------------------------------------------------------------------
#--------------------------------------------------------------------------------
# Inputs
#--------------------------------------------------------------------------------
# lfalls_flow - estimate of observed flow at Little Falls
#    without the release
# mos - margin of safety to be used to compute need
#
#--------------------------------------------------------------------------------
# Output
#--------------------------------------------------------------------------------
# need at Little Falls, including flowby and MOS (MGD)
#--------------------------------------------------------------------------------
#
estimate_need_func <- function(lfalls_flow, mos){
  need <- lfalls_flowby + mos - lfalls_flow
  need <- if_else(need > 0, need, 0, missing = 0)    
  return(need)
}